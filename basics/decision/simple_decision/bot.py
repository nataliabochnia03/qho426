print("Towards which direction should I paint (up, down, left or right?")
x = input()
if x == "up":
  print("I am painting in the upward direction!")
elif x == "down":
  print("I am painting in downard direction!")
elif x == "left":
  print("I am painting in left direction!")
elif x == "right":
  print("I am painting in right direction!")
else:
  print("I am done! Look what I painted!")